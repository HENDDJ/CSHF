<template>
    <section>
        <CommonCRUD :columns="formColumns" api-root="identity/partyMemberExcellent" :formColumns="formColumns" :queryFormColumns="queryColumns">
        </CommonCRUD>
    </section>
</template>

<script>
    import LookUp from '@/lookup';
    import CommonCRUD from '@/components/CommonCRUD';
    export default {
        name: 'PartyMemberExcellent',
        created(){
            this.formColumns = this.$store.state.classInfo.properties
            this.handleSelectOptions();
        },
        methods: {
            handleSelectOptions() {
                let  items = [
                    ['userSex', 'Sex'],
                    ['education', 'Education'],
                ].forEach(item => {
                        this.formColumns.filter(sub => sub.name === item[0])[0].options = LookUp[item[1]]
                    }
                )
                this.formColumns.forEach(item => {
                    if(item.transferType === 'lookup') {
                        item.formatter = (row, cell,val,index) => {
                            return LookUp[item.lookupKey].filter(subitem => subitem.value === val)[0].label;
                        }
                    }
                });
            }
        },
        components: {
            CommonCRUD
        }
    };
</script>

